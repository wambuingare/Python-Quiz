
def add(a,b):
    answer=a+b
    return answer

def subtract(a,b):
    answer=a-b
    return answer

def multiply(x,y):
    answer=x*y
    return answer 

def divide(w,z):
    answer=w/z
    return answer

def modulus(q,r):
    answer=q/r
    return answer

def exponent(t,v):
    answer=t**v
    return answer

def intdivide(a,c):
    answer=a//c
    return answer

def square(f):
    answer=f*f
    return answer

def cube(k):
    answer=k*k*k
    return answer

def factorial(a):
    fact=1
    for num in range(1,a+1):
        fact*=num
    return fact


